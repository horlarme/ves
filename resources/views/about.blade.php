                    <div class="col-sm-7">
                        <p class="lead">Ehikhamenor is a multi-faceted artist whose practice encompasses painting,
                            drawing,
                            photography, sculpture and installation, as well as unique perforated works on paper.</p>
                        <p>Hailing from Udomi-Uwessan, Edo State,the historic seat of the Benin Empire, he draws
                            inspiration
                            from the dual aesthetic and spiritual traditions which infused his upbringing, using imagery
                            and
                            symbolism from both Edo traditional religion and Catholicism.<br/>Ehikhamenor has been
                            prolific
                            in producing abstract, symbolic and politically motivated works. An award-winning writer,
                            his
                            book ‘Excuse Me!’, a satirical non-fiction, is a recommended text at two Nigerian
                            universities.
                        </p>
                        <p>A 2016 Rockefeller Foundation Bellagio Fellow, Ehikhamenor has held several solo exhibitions
                            and
                            his work has been included in numerous group exhibitions and biennales, including the 57th
                            Venice Biennale as part of the Nigerian Pavilion (2017), 5th Mediations Biennalein Poznan,
                            Poland (2016), the12th Dak’art Biennale in Dakar, Senegal (2016), Biennale Jogja XIII,
                            Indonesia
                            (2015), and the 56thVenice Biennaleas part of the German Pavilion (2015).</p>
