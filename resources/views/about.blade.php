            <div class="text-center panel-heading">
                <h3 class="panel-title">
                    <a href="#biography" class="panel-title panel-toggle collapsed" data-toggle="collapse">Biography</a>
                </h3>
            </div>
            <div class="row panel-collapse collapse" id="biography">
                <div class="panel-body">
                    <!-- /.span3 -->
                    <div class="col-sm-12">
                        <p class="lead">Victor Ehikhamenor is an artist, photographer and writer born in the village of
                            Udomi-Uwessan, Nigeria. Ehikhamenor has been prolific in producing abstract, symbolic and
                            politically motivated works.</p>
                        <p>His works are influenced by duality of African traditional religion and the interception of
                            Western beliefs, memories and nostalgia. His symbols and motifs are reminiscent of his
                            childhood
                            village shrines art as well as the Catholicism that was inherent in his village.
                            Ehikhamenor’s
                            images always work as a proliferation of forms – of figures, shapes and appearances. His
                            canvasses are folded into deliberate forms, becoming personable objects on display.</p>
                        <p>Ever the storyteller, regardless of how his works are rendered on the surface – by charcoal,
                            acrylic, enamel, nail perforations, video installations – his intent is that these images
                            portend an enchanted world inspired by everyday life both urban and rural, folktales,
                            stories,
                            histories real and imagined.</p>
                        <p>Ehikhamenor has had numerous exhibitions with strong following in Nigeria, the United States,
                            Asia, the UK and other parts of Europe. His fictional work “Madam” and drawings “Postcards
                            from
                            Italy” were part of the German pavilion in the 56th Venice Biennale, “All the World's
                            Future”,
                            curated by Okwui Enwezor in 2015. He was also one of the eleven Nigerian artists at the 2015
                            Jogja Biennale in Indonesia where he presented his monumental installation, “The Wealth of
                            Nations”. One of the three artists at the first Nigerian Pavilion in the Venice Biennale, “A
                            Biography of the Forgotten” was the work showcased at the the 57th Venice Biennale
                            (2017).</p>
                        <p>As a writer he has published numerous fiction and critical essays with academic journals,
                            mainstream magazines and newspapers round the world including New York Times, BBC, CNN
                            Online,
                            Washington Post, Farafina, AGNI magazine, Wasafiri Magazine and others. Ehikhamenor was the
                            creative director of the award winning Next Newspapers in Lagos, where he also maintained
                            the
                            socio-political satiric column, Excuse Me! He has designed and redesigned many other
                            newspapers
                            like the national newspaper Daily Times where he was the CEO and Editor-in-Chief. He has
                            also
                            designed book covers for award winning authors and organizations like The Caine Prize for
                            Africa
                            Fiction in London, Obsidian Literary Magazine, Akashic Publishers, NY, Chimamanda Adichie,
                            Helon
                            Habila, Chika Unigwe, Lola Shoneyin and many others.In 2013, Ehikhamenor collaborated with
                            the
                            celebrated Nigerian fashion designer Ituen Basi to create the eclectic Ekemini collection
                            which
                            has been featured in Vogue Italia, Mercedes-Benz Fashion show and more. In 2015, he started
                            a
                            peace initiative, INK NOT BLOOD, using art, writing, cartoons and lectures to promote
                            national
                            peace in Nigeria. His book EXCUSE ME!, a satirical creative non-fiction view of life as a
                            Nigerian both at home and abroad is a recommended text in two Nigerian universities.
                            Ehikhamenor
                            maintains a studio in Lagos, Nigeria.</p>
